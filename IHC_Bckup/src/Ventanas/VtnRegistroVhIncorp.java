    private void btnCredencialAnverActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnCredencialAnverActionPerformed
    {//GEN-HEADEREND:event_btnCredencialAnverActionPerformed
        JFileChooser credAnver = new JFileChooser();
        int opc = credAnver.showOpenDialog(this);
        if (opc == JFileChooser.APPROVE_OPTION)
        {
            String ruta = credAnver.getSelectedFile().getPath();
            rsscalelabel.RSScaleLabel.setScaleLabel(lblCredencialAnver, ruta);
            lblCredencialAnver.setText("");
            lblTxtCredencialAnver.setText(credAnver.getSelectedFile().getName());
            credencialAnver = ruta;
        } else
        {
            System.out.println("No se selecciono ningun archivo.");
        }
    }//GEN-LAST:event_btnCredencialAnverActionPerformed

