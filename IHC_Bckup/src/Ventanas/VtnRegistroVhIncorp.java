    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton7ActionPerformed
    {//GEN-HEADEREND:event_jButton7ActionPerformed
        imagen1 = (lblCredencialAnver.getIcon() != null);
        imagen2 = (lblCredencialRever.getIcon() != null);
        imagen3 = (lblTarjCirculacion.getIcon() != null);
        tamanio = (String) cBTamanio.getSelectedItem();
        System.out.println("Tamanio == " + tamanio);
        if (!jTNombre.getText().isEmpty()
                && (!jTPlacas.getText().isEmpty() && !jTPlacas.getText().equals("Ingrese el numero de placas"))
                && (!jTMarca.getText().isEmpty() && !jTMarca.getText().equals("Ingrese la marca"))
                && (!jTColor.getText().isEmpty() && !jTColor.getText().equals("Ingrese el color"))
                && (!jTNombre4.getText().isEmpty() && !jTNombre.getText().equals("Ingrese nombre completo aqui"))
                && (imagen1 == true && imagen2 == true && imagen3 == true)
                && (cBTamanio.getSelectedItem().equals("Pequeño")
                || cBTamanio.getSelectedItem().equals("Mediano")
                || cBTamanio.getSelectedItem().equals("Grande")))
        {
            //Proceso de guardar en la BD
            MetodosCRUD.guardaBD(jTNombre4.getText(), nombre, credencialAnver, credencialRever, tarjCirculacion, placas, color, marca, tamanio);
            //Proceso de limpiado de ventana
            lblCredencialAnver.setIcon(null);
            lblCredencialRever.setIcon(null);
            lblTarjCirculacion.setIcon(null);
            lblNoRegistro.setText(String.valueOf(MetodosCRUD.ultimoRegistro(1) + 1));
            lblTxtCredencialAnver.setText("No ha seleccionado ninguna imagen");
            lblTxtCredencialRever.setText("No ha seleccionado ninguna imagen");
            lblTxtTarjCirculacion.setText("No ha seleccionado ninguna imagen");
            jTNombre.setText("Ingrese nombre completo aqui");
            jTMarca.setText("Ingrese la marca");
            jTColor.setText("Ingrese el color");
            jTPlacas.setText("Ingrese el numero de placas");
            imagen1 = false;
            imagen2 = false;
            imagen3 = false;
            nombre = "";
            credencialAnver = "";
            credencialRever = "";
            tarjCirculacion = "";
            placas = "";
            color = "";
            marca = "";
            tamanio = "";
            cBTamanio.setSelectedIndex(0);
        } else
        {
            if (jTNombre.getText().isEmpty()
                    || !jTPlacas.getText().isEmpty()
                    || !jTMarca.getText().isEmpty()
                    || !jTColor.getText().isEmpty()
                    || !jTNombre4.getText().isEmpty()
                    || (imagen1 == true && imagen2 == true && imagen3 == true)
                    || (imagen1 == false && imagen2 == false && imagen3 == false)
                    && (cBTamanio.getSelectedItem().equals("Pequeño")
                    || cBTamanio.getSelectedItem().equals("Mediano")
                    || cBTamanio.getSelectedItem().equals("Grande")))
            {
                ErrorCampos codigo = new ErrorCampos(null, true);
                codigo.setModal(true);
                codigo.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

